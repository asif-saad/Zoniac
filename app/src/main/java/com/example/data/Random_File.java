package com.example.data;

public class Random_File {

    int val;
    String name;

    public Random_File(int val, String name) {
        this.val = val;
        this.name = name;
    }

    public int getVal() {
        return val;
    }

    public String getName() {
        return name;
    }

    public void setVal(int val) {
        this.val = val;
    }

    public void setName(String name) {
        this.name = name;
    }
}

package com.example.data;

public class Random_File {

    int val;
    double result;

    public Random_File(int val, double result) {
        this.val = val;
        this.result = result;
    }

    public int getVal() {
        return val;
    }


    public double getResult() {
        return result;
    }

    public void setVal(int val) {
        this.val = val;
    }


    public void setResult(double result) {
        this.result = result;
    }
}
